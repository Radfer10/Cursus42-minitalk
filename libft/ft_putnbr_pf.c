#include "ft_printf.h"

void	ft_putnbr_pf(int n, int *i)
{
	if (n == -2147483648)
		ft_putstr_pf("-2147483648", i);
	if (n < 0 && n != -2147483648)
	{
		ft_putchar_pf('-', i);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_pf(n / 10, i);
		n %= 10;
	}
	if (n != -2147483648)
		ft_putchar_pf(n + '0', i);
}
